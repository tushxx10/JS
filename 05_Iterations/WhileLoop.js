//While Loop

let index=0
while (index <= 10) {
    //console.log(`Value of index is ${index}`);
    index +=2;
}

let myArray = ['Flash' , 'Superman' , 'Spiderman']
let arr = 0
while(arr < myArray.length){
    //console.log(`Value of array is ${myArray[arr]}`);
    arr++;
}

//DO WHILE Loop

let score = 1;
do {
    console.log(`Value is ${score}`);
    score+=1;
} while (score <= 10);